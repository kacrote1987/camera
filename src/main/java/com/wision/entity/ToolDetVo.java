package com.wision.entity;

public class ToolDetVo {
    private Long rowNum;
    private Long toolId;
    private String toolType;
    private String toolName;
    private String toolVer;
    private String toolExplain;
    private String toolTime;
    private String shareNum;
    private String toolState;

    public Long getRowNum() {
        return rowNum;
    }

    public void setRowNum(Long rowNum) {
        this.rowNum = rowNum;
    }

    public Long getToolId() {
        return toolId;
    }

    public void setToolId(Long toolId) {
        this.toolId = toolId;
    }

    public String getToolType() {
        return toolType;
    }

    public void setToolType(String toolType) {
        this.toolType = toolType;
    }

    public String getToolName() {
        return toolName;
    }

    public void setToolName(String toolName) {
        this.toolName = toolName;
    }

    public String getToolVer() {
        return toolVer;
    }

    public void setToolVer(String toolVer) {
        this.toolVer = toolVer;
    }

    public String getToolExplain() {
        return toolExplain;
    }

    public void setToolExplain(String toolExplain) {
        this.toolExplain = toolExplain;
    }

    public String getToolTime() {
        return toolTime;
    }

    public void setToolTime(String toolTime) {
        this.toolTime = toolTime;
    }

    public String getShareNum() {
        return shareNum;
    }

    public void setShareNum(String shareNum) {
        this.shareNum = shareNum;
    }

    public String getToolState() {
        return toolState;
    }

    public void setToolState(String toolState) {
        this.toolState = toolState;
    }
}
