package com.wision.entity;

public class ConVo {
    private Long conId;
    private String conName;
    private String linkName;
    private String conLink;
    private Long conNece;

    public Long getConId() {
        return conId;
    }

    public void setConId(Long conId) {
        this.conId = conId;
    }

    public String getConName() {
        return conName;
    }

    public void setConName(String conName) {
        this.conName = conName;
    }

    public String getLinkName() {
        return linkName;
    }

    public void setLinkName(String linkName) {
        this.linkName = linkName;
    }

    public String getConLink() {
        return conLink;
    }

    public void setConLink(String conLink) {
        this.conLink = conLink;
    }

    public Long getConNece() {
        return conNece;
    }

    public void setConNece(Long conNece) {
        this.conNece = conNece;
    }
}
