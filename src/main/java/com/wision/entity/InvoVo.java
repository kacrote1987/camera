package com.wision.entity;


public class InvoVo {
    private Long rowNum;
    private Long invoId;
    private String invoType;
    private String invoNo;
    private String invoSurp;
    private String invoAppman;
    private String invoDate;
    private String invoNote;

    public Long getRowNum() {
        return rowNum;
    }

    public void setRowNum(Long rowNum) {
        this.rowNum = rowNum;
    }

    public Long getInvoId() {
        return invoId;
    }

    public void setInvoId(Long invoId) {
        this.invoId = invoId;
    }

    public String getInvoType() {
        return invoType;
    }

    public void setInvoType(String invoType) {
        this.invoType = invoType;
    }

    public String getInvoNo() {
        return invoNo;
    }

    public void setInvoNo(String invoNo) {
        this.invoNo = invoNo;
    }

    public String getInvoSurp() {
        return invoSurp;
    }

    public void setInvoSurp(String invoSurp) {
        this.invoSurp = invoSurp;
    }

    public String getInvoAppman() {
        return invoAppman;
    }

    public void setInvoAppman(String invoAppman) {
        this.invoAppman = invoAppman;
    }

    public String getInvoDate() {
        return invoDate;
    }

    public void setInvoDate(String invoDate) {
        this.invoDate = invoDate;
    }

    public String getInvoNote() {
        return invoNote;
    }

    public void setInvoNote(String invoNote) {
        this.invoNote = invoNote;
    }
}
