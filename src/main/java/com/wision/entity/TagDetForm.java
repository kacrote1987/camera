package com.wision.entity;

public class TagDetForm {
    private String action;
    private Long tagId;
    private String tagNo;
    private String tagName;
    private String deptName;
    private String tagContent;
    private String tagState;
    private String camResol;
    private String camAlgo;

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public Long getTagId() {
        return tagId;
    }

    public void setTagId(Long tagId) {
        this.tagId = tagId;
    }

    public String getTagNo() {
        return tagNo;
    }

    public void setTagNo(String tagNo) {
        this.tagNo = tagNo;
    }

    public String getTagName() {
        return tagName;
    }

    public void setTagName(String tagName) {
        this.tagName = tagName;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }

    public String getTagContent() {
        return tagContent;
    }

    public void setTagContent(String tagContent) {
        this.tagContent = tagContent;
    }

    public String getTagState() {
        return tagState;
    }

    public void setTagState(String tagState) {
        this.tagState = tagState;
    }

    public String getCamResol() {
        return camResol;
    }

    public void setCamResol(String camResol) {
        this.camResol = camResol;
    }

    public String getCamAlgo() {
        return camAlgo;
    }

    public void setCamAlgo(String camAlgo) {
        this.camAlgo = camAlgo;
    }
}
